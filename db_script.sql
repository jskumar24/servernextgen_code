-- Create database

CREATE DATABASE IF NOT EXISTS servernexgen_db;

-- Use the database
USE servernexgen_db;

-- Create table tbl_shared_hosting

CREATE TABLE tbl_shared_hosting ( id INT AUTO_INCREMENT PRIMARY KEY,
plan_name VARCHAR(255) NOT NULL,
plan_price DECIMAL(10, 2) NOT NULL,
website_count INT NULL,
harddisk_space VARCHAR(50) NULL,
bandwidth VARCHAR(50) NULL,
databases_count INT NULL,
users_count INT NULL,
email_accounts_count INT NULL);

-- Create table tbl_dedicated_vps

CREATE TABLE tbl_dedicated_vps ( id INT AUTO_INCREMENT PRIMARY KEY,
plan_name VARCHAR(255) NOT NULL,
plan_type VARCHAR(100) NOT NULL,
plan_price DECIMAL(10, 2) NOT NULL,
cpu VARCHAR(50) NULL,
cpu_cores INT NULL,
harddisk_space VARCHAR(50) NULL,
bandwidth VARCHAR(50) NULL,
cpu_ram VARCHAR(50) NULL);

